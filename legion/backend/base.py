from typing import Protocol


class Backend(Protocol):
    name: str

    # array ops
    def array(self, x): ...
    def zeros(self, shape): ...
    def ones(self, shape): ...
    def concatenate(self, xs, axis=0): ...
    def clip(self, x, min, max): ...
    def norm(self, x, axis=None): ...
    def exp(self, x): ...
    def sin(self, x): ...
    def cos(self, x): ...
    def sum(self, x): ...
    def any(self, x): ...
    def all(self, x): ...

    # execution transforms
    def jit(self, fn, **kwargs): ...
